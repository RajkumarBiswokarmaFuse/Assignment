def addition(num1 , num2):
    """ This function takes an input of two numbers and returns its sum"""
    return num1 + num2

def subtraction(num1, num2):
    """ This function takes an input of two numbers and returns its difference """
    return num1-num2

def multiplication(num1, num2):
    """This function takes an input of two numbers and returns its product"""
    return num1 * num2